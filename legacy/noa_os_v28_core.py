class NOAKernel:
    """
    The heart of NOA OS.
    Manages state containers atomically.
    """
    def __init__(self):
        self._sessions: Dict[str, ContextState] = {}
        self._ledger = AegisLedger()
        self._lock = threading.Lock()
        self._start_time = time.time()

        # Boot log
        self._ledger.record("KERNEL_BOOT", {"version": NOA_VERSION})

    def start_session(self, session_id: str = "default") -> str:
        with self._lock:
            if session_id in self._sessions:
                return session_id

            initial_state = ContextManager.create_session(session_id)
            self._sessions[session_id] = initial_state

            self._ledger.record("SESSION_START", {
                "session_id": session_id,
                "init_hash": initial_state.state_hash
            })
            return session_id

    def process_input(self, session_id: str, text: str) -> str:
        """
        Atomic Input Processing Transaction.
        1. Fetch State
        2. Validate
        3. Transition
        4. Commit
        """
        # 1. Atomic Read
        with self._lock:
            current_state = self._sessions.get(session_id)
            if not current_state:
                raise ContextIntegrityError(f"Session {session_id} not found")

        # 2. Transition (Pure Logic - Safe outside lock if no contention,
        #    but we lock for strict serializability in this version)
        with self._lock:
            # Re-fetch to be safe in high concurrency or use optimistic locking
            current_state = self._sessions[session_id]

            # User Message
            next_state = ContextManager.append_message(current_state, Role.USER, text)

            # System Logic (Mocking Logic Core)
            # In real NOA, this would call Policy Engine / LLM
            response_text = f"ECHO: {text}"
            final_state = ContextManager.append_message(next_state, Role.ASSISTANT, response_text)

            # 3. Commit
            self._sessions[session_id] = final_state

            # 4. Audit
            self._ledger.record("INTERACTION", {
                "session_id": session_id,
                "input_hash": next_state.history[-2].fingerprint,
                "output_hash": final_state.history[-1].fingerprint,
                "state_v": final_state.version
            })

            return response_text

    def get_system_status(self) -> SystemStatus:
        """Returns immutable system status."""
        with self._lock:
            count = len(self._sessions)

        return SystemStatus(
            cpu_load_proxy=0.1,  # Mock
            memory_usage_proxy=0.2, # Mock
            active_sessions=count,
            kernel_uptime=time.time() - self._start_time
        )

    def dump_ledger(self) -> str:
        """Exports the cryptographic ledger tail."""
        return self._ledger.get_tail_hash()



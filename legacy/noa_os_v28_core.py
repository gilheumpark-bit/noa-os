@dataclass(frozen=True)
class MessageFrame:
    """Immutable message unit."""
    msg_id: str
    role: Role
    content: str
    timestamp: float
    metadata: Dict[str, Any] = field(default_factory=dict)

    @property
    def fingerprint(self) -> str:
        payload = f"{self.msg_id}|{self.role.name}|{self.content}|{self.timestamp}"
        return hashlib.sha256(payload.encode()).hexdigest()


@dataclass(frozen=True)
class ContextState:
    """
    Immutable snapshot of a conversation context.
    Any update creates a NEW ContextState instance.
    """
    session_id: str
    history: Tuple[MessageFrame, ...]
    variables: Dict[str, Any]  # Note: In strict ACE, this should be frozen too
    last_updated: float
    version: int
    prev_hash: str

    @property
    def state_hash(self) -> str:
        """Computes the integrity hash of the current state."""
        raw = (
            f"{self.session_id}|{self.version}|{self.prev_hash}|"
            f"{self.last_updated}|{len(self.history)}"
        )
        return hashlib.sha256(raw.encode()).hexdigest()



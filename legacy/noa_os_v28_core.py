class ContextManager:
    """
    Stateless logic for Context transitions.
    """
    @staticmethod
    def create_session(session_id: str) -> ContextState:
        return ContextState(
            session_id=session_id,
            history=(),
            variables={},
            last_updated=time.time(),
            version=0,
            prev_hash="GENESIS"
        )

    @staticmethod
    def append_message(
        state: ContextState,
        role: Role,
        content: str
    ) -> ContextState:
        """
        Returns a NEW ContextState with the message appended.
        Does not mutate the old state.
        """
        new_msg = MessageFrame(
            msg_id=str(uuid.uuid4()),
            role=role,
            content=content,
            timestamp=time.time()
        )

        # Enforce history limit
        new_history = state.history + (new_msg,)
        if len(new_history) > MAX_CONTEXT_HISTORY:
            new_history = new_history[-MAX_CONTEXT_HISTORY:]

        return replace(
            state,
            history=new_history,
            last_updated=time.time(),
            version=state.version + 1,
            prev_hash=state.state_hash
        )



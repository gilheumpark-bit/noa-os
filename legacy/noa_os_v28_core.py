class AegisLedger:
    """
    Centralized, thread-safe, append-only log.
    Uses Hash Chaining for integrity.
    """
    def __init__(self):
        self._chain: List[str] = []
        self._events: List[Dict[str, Any]] = []
        self._lock = threading.Lock()
        self._last_hash = hashlib.sha256(b"NOA_GENESIS").hexdigest()

    def record(self, event_type: str, payload: Dict[str, Any]) -> str:
        timestamp = time.time()
        # Ensure deterministic JSON
        payload_str = json.dumps(payload, sort_keys=True, default=str)

        with self._lock:
            # H(Prev + Event)
            raw = f"{self._last_hash}|{timestamp}|{event_type}|{payload_str}"
            event_hash = hashlib.sha256(raw.encode()).hexdigest()

            self._chain.append(event_hash)
            self._events.append({
                "hash": event_hash,
                "ts": timestamp,
                "type": event_type,
                "data": payload
            })
            self._last_hash = event_hash
            return event_hash

    def get_tail_hash(self) -> str:
        with self._lock:
            return self._last_hash


